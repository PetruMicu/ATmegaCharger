/**
* Calls local functions based on sensor
*
*/
uint16 HW_ReadSensor(HW_AIN sensor) {
    switch (sensor) {
        case CURR_SENS1:
            return HW_ReadCurrent1();
        case CURR_SENS2:
            return HW_ReadCurrent2();
        case TMP_SENS:
            return HW_ReadTemperature();
        default:
            return 0u;
    }
}

